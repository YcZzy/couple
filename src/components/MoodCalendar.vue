// ÂΩìÂâçÊó•Êúü
const today = dayjs().format('YYYY-MM-DD');

// ÈöèÊú∫ÈóÆÂÄôËØ≠
const greetings = [
  '‰ªäÂ§©ÁöÑÂøÉÊÉÖÂ¶Ç‰ΩïÂë¢Ôºü',
  'ËÆ∞ÂΩï‰∏ãÊ≠§ÂàªÁöÑÊÉÖÁª™ÂêßÔΩû',
  'ÊØè‰∏ÄÂ§©ÈÉΩÂÄºÂæóÁèçËóèÔΩû',
  '‰∏ÄËµ∑Áïô‰∏ãÁæéÂ•ΩÂõûÂøÜÔΩû',
  'ÂàÜ‰∫´‰Ω†ÁöÑÂñúÊÄíÂìÄ‰πêÔΩû',
  'ËÆ©ÂøÉÊÉÖÊúâÂ§ÑÂÆâÊîæÔΩû'
];

