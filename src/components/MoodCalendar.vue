const router = useRouter();
const store = useMoodStore();

// Á°Æ‰øùstoreÂàùÂßãÂåñ
onMounted(() => {
  store.init();
});

// ÂΩìÂâçÊòæÁ§∫ÁöÑÊúà‰ªΩ
const currentMonth = ref(dayjs());

// Êó•ÊúüÊ†ºÂºèÂåñ
const formatMonth = computed(() => {
  return currentMonth.value.format('YYYYÂπ¥MÊúà');
